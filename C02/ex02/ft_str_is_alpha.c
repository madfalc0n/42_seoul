int			ft_str_is_alpha(char *str)
{
	char	a;

	while (1)
	{
		a = *str;
		if (a == '\0')
			break ;
		if (!(a >= 'A' && a <= 'Z') &&
				!(a >= 'a' && a <= 'z'))
			return (0);
		str++;
	}
	return (1);
}
